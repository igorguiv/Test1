# Пахолок Вікторія
# Функція розпізнає, чи є рядок дійсним числом.

def Число_або_ні (Рядок):  # def означає, що це функція
    try:  # спроба перетворення
        float(Рядок)
        return True
    except ValueError:
        return False


# Тестування функції
t = Число_або_ні(5)
print(t)

t = Число_або_ні("5")
print(t)

t = Число_або_ні("Роман Тимчук")
print(t)

